Color colorFromHex(String hexColor) {
  hexColor = hexColor.replaceAll('#', '').replaceAll('0x', '');
  if (hexColor.length == 6) hexColor = 'FF$hexColor';
  return Color(int.parse(hexColor, radix: 16));
}

String colorHex(String color) {
  return color.replaceAll('0x', '').replaceAll('0X', '');
}

Color colorValue(String color) {
  try {
    final hex = color.replaceAll('0x', '').replaceAll('0X', '');
    return Color(int.parse(hex.length == 6 ? 'FF$hex' : hex, radix: 16));
  } catch (_) {
    return ColorConst.colorFF0066FF;
  }
}
